package org.example.javaPreperation.advanceJava.exercise;

public class Swap2Number {
    private int number1;
    private int number2;
    public Swap2Number(int num1,int num2){
        this.number1 = num2;
        this.number2 = num1;
    }
    public int getNumber1(){
        return this.number1;
    }
    public int getNumber2(){
        return this.number2;
    }
}
