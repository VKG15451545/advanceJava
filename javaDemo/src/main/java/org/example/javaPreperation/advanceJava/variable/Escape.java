package org.example.javaPreperation.advanceJava.variable;

public class Escape {
    static void main(String[] args) {
        System.out.println("Hello \"VKCoding\"...");
        System.out.println("Hello \nVKCoding\"...");
        System.out.println("Hello\tVKCoding\"...");
        System.out.println("Hello\bVKCoding\"...");
        System.out.println("Hello\\VKCoding\"...");
        System.out.println("Right Half Pyramid");
        System.out.println("*\n**\n***\n***\n****\n*****");
        System.out.println("Reverse Right Half Pyramid");
        System.out.println("*****\n****\n***\n**\n*");
        System.out.println("Left Half Pyramid");
        System.out.println("    *\n   **\n  ***\n ****\n*****");
    }

}
