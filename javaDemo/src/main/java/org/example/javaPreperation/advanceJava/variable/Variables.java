package org.example.javaPreperation.advanceJava.variable;

public class Variables {
     static void main(String[] args) {
        int myNumber = 4343; // identifier  (int: dataType,myNumber: identifier,4343: literal)
        int yourNumber;
        yourNumber =353;
         System.out.println(yourNumber);
         yourNumber = 43;
         System.out.println(yourNumber);
         boolean isVegetarian = true;
         System.out.println(isVegetarian);

         //Naming Conventions:
         /*
            camelCase :   myVariableName
            snake_case : my_variable_name
            Kebab-case : my-variable-name
         */

         // Identifier Rules
         /*
            1. allowed character : A-Z,a-z,0-9,$,_
            2. keywords not allowed
            3. should not start with number
            4. caseSensitive

          */
         //Keywords:
         /*
         default super switch strictfp void static break protected case implements assert throw native throws
         class impact catch transient double try new enum goto long const byte char float short interface return
         package interface continue public finally import boolean private instanceof int extends synchronized abstract
         volatile final
          */


    }
}
